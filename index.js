class Scooter {
  constructor(year, color, model) {
    this.year = year;
    this.color = color;
    this.model = model;
  }
}

class Driver {
  constructor(name, age, experience) {
    this.age = age
    this.name = name
    this.experience = experience
  }
}

class PickupLocation {
  constructor(address, city) {
    this.address = address
    this.city = city
  }
}
